async function updateIssue(key, summary, description) {
  await client.put(`/issue/${key}`, {
    fields: { summary, description },
  });
}

async function main() {
  let updated = 0;
  let failed = 0;

  for (const item of updates) {
    try {
      await updateIssue(item.key, item.summary, item.description);
      updated++;
      process.stdout.write(`\rUpdated ${updated}/${updates.length}: ${item.key}   `);
    } catch (e) {
      failed++;
      const msg = e.response?.data
        ? JSON.stringify(e.response.data).slice(0, 120)
        : e.message;
      console.error(`\nFailed ${item.key}: ${msg}`);
    }
  }

  console.log(`\n\n=== JS STACK UPDATE COMPLETE ===`);
  console.log(`Updated: ${updated} | Failed: ${failed}`);
  console.log(`Board: https://${domain}/jira/software/projects/KAN/boards/3/backlog`);
}

main().catch((e) => {
  console.error("Script error:", e.response?.data || e.message);
  process.exit(1);
});
